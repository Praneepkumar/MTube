export const getRandomMessages = () => {
  const ranNum = Math.floor(Math.random() * 5 + 0);
  const randomMsg = [
    "Lorem ipsum dolor en visto",
    "It is a random text",
    "Lorem can generate text",
    "Make use of Lorem text",
    "Generate random text wuth lorem",
  ];

  let currentMsg = randomMsg[Math.floor(Math.random() * 5 + 0)];
  let prevMsg = "";

  currentMsg = randomMsg[ranNum];
  if (prevMsg === currentMsg) {
    let anotherMsg = "";
    if (ranNum >= 0 && ranNum < 4) {
      anotherMsg = randomMsg[ranNum + 1];
    } else {
      anotherMsg = randomMsg[ranNum - 1];
    }
    prevMsg = anotherMsg;
    return anotherMsg;
  } else {
    prevMsg = currentMsg;
    return currentMsg;
  }
};
